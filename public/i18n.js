(function () {
  const root = document.querySelector('main') || document.body;
  let snapshot = null;

  function takeSnapshot() {
    snapshot = [];
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        if (!node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
        const p = node.parentElement;
        if (!p || p.closest('script,style,select,.notranslate')) return NodeFilter.FILTER_REJECT;
        return NodeFilter.FILTER_ACCEPT;
      }
    });
    while (walker.nextNode()) snapshot.push([walker.currentNode, walker.currentNode.nodeValue]);
  }

  function apply(lang) {
    if (!snapshot) takeSnapshot();
    const dict = TRANSLATIONS[lang];
    snapshot.forEach(([node, original]) => {
      const key = original.trim();
      node.nodeValue = dict && dict[key] ? original.replace(key, dict[key]) : original;
    });
    // input placeholder isn't a text node — handle the import field explicitly
    const urlInput = document.getElementById('urlInput');
    if (urlInput) {
      const ph = { es: 'Ingresa tu URL de Behance/portafolio', fr: 'Saisissez votre URL Behance/portfolio', de: 'Gib deine Behance-/Portfolio-URL ein', pt: 'Insira sua URL do Behance/portfólio' };
      urlInput.placeholder = ph[lang] || 'Enter your Behance/Portfolio URL';
    }
    document.documentElement.lang = lang || 'en';
  }

  const select = document.getElementById('langSelect');
  const saved = localStorage.getItem('killawork:lang') || 'en';
  if (select && [...select.options].some(o => o.value === saved)) select.value = saved;
  if (saved !== 'en') apply(saved);
  select?.addEventListener('change', () => {
    localStorage.setItem('killawork:lang', select.value);
    apply(select.value);
  });
})();
